
num1 = int(input("Digite o primeiro número inteiro:"))

num2 = int(input("Digite o segundo número inteiro:"))

num3 = float(input("Digite o terceiro número real:"))

num4 = (num1 * 2) * (num2 / 2)

num5 = (num1 * 3) + num3

num6 = num3 ** 3

print(f"O produto do dobro do primeiro com metade do segundo: {num4}")

print(f"A soma do triplo do primeiro com o terceiro: {num5}")

print(f"O terceiro elevado ao cubo: {num6}")
